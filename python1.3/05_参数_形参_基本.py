#坑:如果你的默认值参数是一个可变的数据类型,会被共享
def func(val,lst=[]):
    lst.append(val)
    print(lst)
func(10086) #[10086]
func(10010)  #[10086, 10010] 两次访问都是同一个列表
func(777888,[])
